public class all_variables {
    public static void main(String[] args) {
        byte b = 100;
        short s = 30000;
        int i = 1000000;
        long l = 15000000000L;
        float f = 5.75f;
        double d = 19.99;
        char c = 'A';
        boolean bool = true;
        String str = "Hello";

        System.out.println(b + " " + s + " " + i + " " + l + " " + f + " " + d + " " + c + " " + bool + " " + str);
    }
}